def pascals(row, rows):
    lst = [0] * rows
    for i in range(rows):
        for j in range(i + 1):
            lst[i] = [1] * (j + 1)

    for i in range(2, rows):
        for j in range(1, i):
            lst[i][j] = lst[i - 1][j - 1] + lst[i - 1][j]
    print(lst[row])

pascals(30, 40)