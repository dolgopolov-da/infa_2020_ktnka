txt = 'a b c d e f'.split()

def show_sublists(list):
    lst = []
    lst.append([])

    for el in txt:
        lst.append([el])

    for i in range(len(txt) - 1):
        for j in range(i + 1, len(txt)):
            sublst = txt[i : j + 1]
            lst.append(sublst)

    lst.sort(key=len)
    print(lst)

show_sublists(txt)



