count = 0
lst_a = [a**3 for a in range(1, 101)]
lst_b = [b**3 for b in range(1, 101)]
lst_c = [c**3 for c in range(1, 101)]
lst_d = [d**3 for d in range(1, 101)]
lst_answer = []

for a in lst_a:
    for b in lst_b:
        for c in lst_c:
            d = a + b - c
            if d in lst_d and a != b and a != c and a != d and b != c and b != d and c != d:
                count += 1
                if a + b not in lst_answer:
                    lst_answer.append(a + b)
                if count == 10:
                    break
        if count == 5:
            break
    if count == 5:
        break

lst_answer.sort()
print(lst_answer[0:5])
