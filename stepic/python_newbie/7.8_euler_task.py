start_time = time.time()

def main():
    lst_a = []
    lst_b = []
    lst_c = []
    lst_d = []
    lst_e = []

    for a in range(1, 151):
        lst_a.append(a**5)
    for b in range(1, 151):
        lst_b.append(b**5)
    for c in range(1, 151):
        lst_c.append(c**5)
    for d in range(1, 151):
        lst_d.append(d**5)
