txt_n = input()
n = int(txt_n[txt_n.find('#')+1:])
lst = []

for i in range(n):
    txt = input()
    if '#' in txt:
        txt = txt[:txt.find('#')]
    lst.append(txt)

for i in range(len(lst)):
    lst[i] = lst[i].rstrip()

print(*lst, sep='\n')